def select_vowels(word):
    letters = ["a", "e", "i", "o", "u"]
    
    if letters[0] in word:
        print(letters[0])
    if letters[1] in word:
        print(letters[1])
    if letters[2] in word:
        print(letters[2])
    if letters[3] in word:
        print(letters[3])
    if letters[4] in word:
        print(letters[4])
    
select_vowels("Lebohang")

